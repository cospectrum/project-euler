i64 find_max_product(const char* number, size_t len, int adj) {
    i64 max_product = 0;
    i64 product = 1;

    struct queue q;
    q.len = adj;
    q.values = malloc(adj * sizeof(i64));

    if (q.values == NULL) {
        printf("error: not enough memory\n");
        return -1;
    }
    set_ones(&q);

    int num;
    for (size_t i = 0; i < len; ++i) {
        num = char_to_int(number[i]);
        if (num == 0) {
            set_ones(&q);
            product = 1;
            continue;
        }

        product /= front(&q);
        push(&q, num);
        product *= num;

        max_product = max(product, max_product);
    }

    free(q.values);
    return max_product;
}

void set_ones(struct queue* q) {
    size_t len = q->len;

    for (size_t i = 0; i < len; ++i)
        q->values[i] = 1;
}

i64 front(const struct queue* q) {
    return q->values[0];
}

void push(struct queue* q, i64 value) {
    size_t len = q->len - 1;

    for (size_t i = 0; i < len; ++i)
        q->values[i] = q->values[i + 1];

    q->values[len] = value;
}

int char_to_int(const char ch) {
    return (int)ch - (int)'0';
}

i64 max(i64 a, i64 b) {
    if (a > b)
        return a;
    return b;
}

